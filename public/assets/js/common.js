function onSubscribe(email) {
 return $.ajax({
    type: "POST",
    url: "api/subscribe",
    data: {'email': email}, // serializes the form's elements.
  });
}

function getCookie(cname) {
  let name = cname + "=";
  let ca = document.cookie.split(';');
  for(let i = 0; i < ca.length; i++) {
    let c = ca[i];
    while (c.charAt(0) == ' ') {
      c = c.substring(1);
    }
    if (c.indexOf(name) == 0) {
      return c.substring(name.length, c.length);
    }
  }
  return "";
}